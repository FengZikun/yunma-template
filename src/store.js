import Vue from 'vue'
import Vuex from 'vuex'
Vue.use(Vuex)
const banner1={
	state:{
		data:{
			picAd1:false,
			showModule:false,
			picNum:1,
			bannerPic:{
				a:null,
				b:null,
			}
		},
		verifyData:{
			verify:false,
			showModule:false,
			cell:{
				a:true,
				b:true,
				c:true,
				d:true,
				e:true,
				f:true,
				g:true,
				h:true,
				i:true,
			}
		},
		fastTrackData:{
			showModule:false,
			fastTrack:false,
			website:true,
			websiteVal:'微官网',
			officialAccounts:true,
			officialAccountsVal:'查看公众号',
			complain:true,
			complainVal:'投诉反馈'
		}
		
	},
	mutations:{
		showPicAd1(state){
			state.data.picAd1=true;
		},
		delThis1(state){
			state.data.picAd1=false;
		},
		module(state,type){
			if(type=='picAd1'){
				state.verifyData.showModule=false;
				state.fastTrackData.showModule=false;
				state.data.showModule=true;
			}
			if(type=='verify'){
				state.data.showModule=false;
				state.verifyData.showModule=true;
				state.fastTrackData.showModule=false;
			}
			if(type=='fastTrack'){
				state.data.showModule=false;
				state.verifyData.showModule=false;
				state.fastTrackData.showModule=true;
			}
		},
		addPicNum(state){
			if(state.data.picNum<=4){
				state.data.picNum++;
			}
		},
		setPic(state,data){
			if(data.type===1){
				state.data.bannerPic.a=data.url;
			}
			if(data.type===2){
				state.data.bannerPic.b=data.url;
			}
		},
		addVerify(state){
			if(!state.verifyData.verify){
				state.verifyData.verify=true;
			}
		},
		addFastTrack(state){
			if(!state.fastTrackData.fastTrack){
				state.fastTrackData.fastTrack=true;
			}
		},
		updateMessage(state,type){
			if(type==='a'){
				state.verifyData.cell.a=!state.verifyData.cell.a
			}
			if(type==='b'){
				state.verifyData.cell.b=!state.verifyData.cell.b
			}
			if(type==='c'){
				state.verifyData.cell.c=!state.verifyData.cell.c
			}
			if(type==='d'){
				state.verifyData.cell.d=!state.verifyData.cell.d
			}
			if(type==='f'){
				state.verifyData.cell.f=!state.verifyData.cell.f
			}
			if(type==='g'){
				state.verifyData.cell.g=!state.verifyData.cell.g
			}
			if(type==='h'){
				state.verifyData.cell.h=!state.verifyData.cell.h
			}
			if(type==='i'){
				state.verifyData.cell.i=!state.verifyData.cell.i
			}
			if(type==='website'){
				state.fastTrackData.website=!state.fastTrackData.website
			}

			if(type==='officialAccounts'){
				state.fastTrackData.officialAccounts=!state.fastTrackData.officialAccounts
			}
			if(type==='complain'){
				state.fastTrackData.complain=!state.fastTrackData.complain
			}
		},
		changeVal(state,obj){
			if(obj.type==='website'){
				state.fastTrackData.websiteVal=obj.value;
			}
			if(obj.type==='officialAccounts'){
				state.fastTrackData.officialAccountsVal=obj.value;
			}
			if(obj.type==='complain'){
				state.fastTrackData.complainVal=obj.value;
			}
		}
	},
	actions:{
		showPicAd1(context){
			context.commit('showPicAd1');
		},
		delThis1(context){
			context.commit('delThis1')
		},
		module(context,type){
			context.commit('module',type)
		},
		setPic(context,data){
			context.commit('setPic',data)
		},
		addPicNum(context){
			context.commit('addPicNum')
		},
		addVerify(context){
			context.commit('addVerify');
		}
	}
}
const store = new Vuex.Store({
	modules: {
		banner1:banner1
	}
})

export default store
